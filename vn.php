là dịp để trả nợ cũ, xóa bỏ xích mích năm cũ, để hướng tới năm mới vui vẻ, hòa 

thuận hơn, làm việc chăm chỉ hơn. <br><br>

Người ta cũng quan niệm rằng, mặc những bộ quần áo mới cũng chính là một 

cách để khởi đầu một năm mới với nhiều niềm vui và may mắn. Tết còn là dịp để 

thờ cúng và thể hiện lòng tôn kính đối với tổ tiên. Nhiều người đi đền, chùa hoặc 

