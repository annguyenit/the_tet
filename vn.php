nhiều gia đình cùng quây quần bên nhau làm bánh chưng thì Tết nay, chúng ta 

có thể mua bánh chưng ở bất cứ nơi nào. Theo FTA vào năm 2013 cho hay, gần 

30% người Việt mua mọi thứ để chuẩn bị cho Tết tại cửa hàng và 21% người nói 

rằng những hoạt động truyền thống để đón Tết hầu như đã biến mất. Nhưng sự 

thay đổi đó không chỉ ở các gia đình mà còn thay đổi ở chính các cửa hàng, thay 

vì đóng cửa toàn bộ thời gian cho việc đón Tết thì nay họ chỉ đóng cửa vào ngày 

