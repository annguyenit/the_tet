người nông dân cùng nghỉ ngơi sau 12 tháng lao động vật vả. Và vào ngày này, 

Tết chính là khoảnh khắc cả đất trời tự làm mới mình và đây chính là ngọn 

nguồn ý nghĩa của những nét văn hóa cổ truyền ngày Tết.

