với đó là thắp nén hương, bày mâm ngũ quả như một cách để tưởng nhớ về tổ 

tiên của mình. Vào đêm giao thừa, các gia đình sẽ cùng nhau thắp nén hương 

cầu khấn Tổ tiên để mời tiền nhân về ăn Tết cùng với con cháu hậu thế. Đây 

