chúng tôi trò chuyện với những thanh thiếu niên, thanh niên và thậm chí là với 

ngay cả những thế hệ lớn tuổi hơn, tất cả họ đều nói với chúng tôi rằng họ cảm 

thấy Tết đã không còn như trước nữa. Tết đã bị đánh mất đi ý nghĩa của bản 

thân nó, cái ý nghĩa rằng đó là dịp để mọi người cùng xôm tụ và quay quần bên 

nhau, cùng nhau sắm sửa, chuẩn bị cho Tết. Cảm giác ấm áp khi tất cả mọi 

người từ trẻ con đến người lớn tuổi ai ai cũng bận rộn, khẩn trương chuẩn bị 

những nghi lễ cho Tết và thậm chí cảnh tất cả các cửa hàng đều đóng cửa trong 

ngày Tết cũng làm cho Tết trở nên đặc biệt hơn. 32,5% những người được khảo 

sát đã nói rằng, họ đã không còn cảm nhận được bầu không khí ấm áp, hạnh 

phúc mà trước đây Tết đã mang đến cho họ.<br><br>

“Khi các cửa hàng đóng cửa trong 2 tuần để đón Tết, chúng tôi phải chuẩn bị đầy 

đủ thức ăn trong những ngày đó, chúng tôi đã mua sắm đầy đủ tất cả những gì 

chúng tôi có thể sẽ cần. Nhưng giờ thì các cửa hàng đã mở ra chỉ ngay sau ngày 

đầu tiên do vậy chúng tôi không cần làm gì cả. Chỉ là có cảm giác không còn 

giống như trước nữa thôi” – Ngọc, 27 tuổi, Tp. HCM<br><br>

Hơn nữa, với các thành phố có nhiều người đến từ nhiều quốc gia khác nhau 

hoặc có phần đông lượng dân nhập cư từ các vùng nông thôn lên thành phố để 

mưu sinh thì Tết còn trở nên vắng vẻ hơn nhiều, như Hà Nội, TP. Hồ Chí Minh 

hay Đà Nẵng. Về một khía cạnh nào đó, Tết đã mất đi năng lượng và sức lan tỏa 

của nó. Và ngày nay, nhiều gia đình thậm chí còn coi Tết như một dịp để đi du 

lịch hoặc nghỉ mát ở các quốc gia khác.<br><br>

“Khi tôi còn nhỏ, cứ mỗi lần Tết đến, mọi con đường đều trở nên đông đúc và 

náo nhiệt – tất cả mọi người đều ùa ra đường để cùng nhau đón Tết hay làm một 

điều gì đó. Nhưng giờ thì đường trở nên vắng vẻ hơn, chúng tôi thậm chí có thể 

nằm lăn ra đường. Tết không vui như hồi trước nữa rồi”. – Hanh, 21 tuổi, 

