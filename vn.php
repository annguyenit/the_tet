những việc làm tốt xấu mà con người đã làm trong năm cũ và báo cáo với Ngọc 

Hoàng những vấn đề tốt xấu của gia chủ. Các gia đình sẽ tiến hành cúng ông 

Táo để có được báo cáo thuận lợi và thả cá chép vàng trong hồ để đưa ông Táo 

về trời.<br><br>

