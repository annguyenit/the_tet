                                                <p class="p-24 three-column">
                                              Trong suốt những năm qua, nhiều tập tục của ngày Tết cổ truyền đã không còn

được giữ lại hoặc bị thay đổi do sự phát triển nhanh chóng của nền kinh tế và xã 

