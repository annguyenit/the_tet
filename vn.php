                                                <p class="p-24 three-column">
                                                   Nhưng dù cho có những thay đổi về mặt tâm lý này thì Tết vẫn luôn là một nét

văn hóa và là một lễ hội thân thương của người Việt Nam, từ trẻ con đến người 

lớn tuổi. Khi được hỏi, có nên hay không nếu nhập chung Tết tây và Tết ta lại với 

nhau, hay nói một cách khác Tết sẽ không còn nữa thì tất cả người Việt Nam 

đều đồng loạt phản đối. Những đề xuất này đã dẫn tới nhiều tranh cãi khá gay 

gắt vào đầu năm nay, khi nó được đưa ra bởi một số nhà kinh tế nổi bật. Có một 

cảm giác rất sâu xa đâu đó rằng “Tết là của chúng ta”, “Tết là một phần trong 

văn hóa và bản sắc truyền thống của dân tộc ta”, “Nếu chúng ta mất Tết chúng 

ta sẽ mất đi một phần của chính chúng ta”. Đây là những ý kiến chúng tôi đã 

nghe được từ chính những bạn thanh thiếu niên Việt Nam, đặc biệt hơn khi họ 

dường như cũng là những người luôn cảm thấy buồn chán nhất mỗi độ Tết đến 

Xuân về.<br><br>

Điều này thể hiện được rằng Tết thực sự có ý nghĩa trong tâm khản của người 

Việt. Từ đó, chúng tôi cũng nhận ra rằng, đối với những người làm Marketing thì 

đây cũng là mùa lễ quan trọng nhất trong năm để tăng doanh số bán. Do đó, việc 

hiểu được sự thay đổi của Tết và biết được nó có ý nghĩa như thế nào là một 

điều rất quan trọng nhằm gắn kết một cách hiệu quả với người Việt trong suốt 

mùa lễ quan trọng này. Điều này sẽ đóng vai trò quan trọng trong cuộc sống của 

người Việt Nam để làm cho Tết trở nên ấm áp như xưa. <br><br>

Một thương hiệu muốn đạt được thành công trong đợt Tết thì thương hiệu đó 

phải nắm bắt được hết những vấn đề còn nhiều bỏ ngõ này và tìm ra cách giải 

quyết bằng cách làm cho những gì thuộc về Tết trở nên phù hợp hơn với bối 

cảnh xã hội ngày nay. Khi chúng ta nhìn vào 4 trong 5 nhãn hàng vào Tết 2015, 

chúng ta đều thấy rằng những thương hiệu này đã thực hiện đúng chính xác 

những gì mà chúng ta cần làm. Coca Cola với năm thứ 4 vẫn giữ vững thương 

hiệu số 1 đã gợi nhắc cho người Việt về ý nghĩa thật sự của Tết – giúp các bạn 

trẻ tìm về lại mái ấm của gia đình và nhận ra rằng thời gian quan trọng hơn tiền 

bạc rất nhiều. OMO cũng đào sâu vào những góc đa chiều khác nhau khi con 

người bị áp lực về mặt kinh tế mỗi lần xa quê trở về nhà để ăn Tết. Nhãn hiệu 

này kể về câu chuyện của cô gái trẻ đã giúp đỡ một cụ già bán hàng rong có đủ 

tiền để trang trải chi phí về quê bởi những chi phí phương tiện giao thông đắt đỏ. 

Neptune thì khai thác việc ngày càng nhiều gia đình xem Tết như một ngày nghỉ 

lễ để họ có thể đi du lịch, đi chơi. Neptune đã thành công trong việc nhắc nhở họ 

rằng họ nên trở về nhà vì nơi đó những thế hệ lớn tuổi và các đấng sinh thành 

vẫn luôn mong ngóng đợi họ về đoàn viên mỗi khi Tết sang. Mặc dù không sử 

dụng các khía cạnh truyền thống của ngày Tết nhưng Vinacafe vẫn tập trung 

nhấn mạnh Tết là dịp để gia đình đoàn tụ. Nhãn hiệu này đã chỉ ra rằng họ hiểu 

những khó khăn của các bạn trẻ và giúp các bạn trẻ bày tỏ điều đó với gia đình 

của họ.<br><br>

Và cứ mỗi năm trôi qua, Phibious chúng tôi đã chứng tỏ rằng chúng tôi có thể 

làm cho chủ đề Tết ngày càng hấp dẫn hơn. Coke vẫn luôn là thương hiệu số 1 

trong 4 năm liền. Vào năm 2012, chúng tôi đã khai thác sâu hơn về khái niệm 

“gia đình” và Coke đã đóng vai trò là người có khả năng kết nối với những nhóm 

lớn hơn để mang gia đình lại gần nhau hơn. Năm 2013, chúng tôi lại hướng cái 

nhìn của mình về những người công nhân phải làm việc vất vả trong suốt cả đợt 

Tết với mong muốn kiếm thêm thu nhập, Coke đã mang gia đình đến với nhau 

bằng cách mang Tết đến với người bố, một người lái xe buýt suốt cả dịp Tết. 

Năm 2014, chúng tôi lại tiếp tục mang đến cho khán giả cái nhìn sâu sắc về 

những bạn trẻ cảm thấy ngày càng chán nản và không thể cùng hòa vào không 

khí Tết với gia đình. Coke đã giúp đỡ những bạn trẻ này thay đổi mình bằng 

cách khuyến khích họ cùng quét dọn nhà cửa và giúp gia đình mình trở nên ấm 

áp hơn. Và cuối cùng vào năm 2015, chúng tôi lại hướng ống kính của mình vào 

những áp lực nặng nề về mặt tài chính của người trưởng thành với mối lo toan 

về những món quà Tết đắt tiền cho gia đình, bạn bè, đồng nghiệp. Coke nhắc 

nhở các gia đình rằng giá trị thời gian mà bạn dành cho gia đình còn quý giá hơn 

gấp bội lần những giá trị tiền bạc mà bạn phải bỏ ra để mua quà Tết.

Với những hứa hẹn của chúng tôi cho Coke trong suốt dịp Tết, chúng tôi ngày 

càng nhận thức sâu sắc hơn về tầm quan trọng của sự hiểu biết những thay đổi 

trong bối cảnh văn hóa Việt Nam. Nhưng đó hoàn toàn không phải là tất cả 

những gì chúng tôi muốn nói.<br><br>

Chúng tôi nhận ra rằng việc chọn ra một đại sứ cho thương hiệu cũng là một 

điều rất quan trọng. Chúng ta cần phải biết những ảnh hưởng mà những thay đổi 

trong việc gắn kết thương hiệu với truyền thông cho khán giả mang lại là gì. Và 

từ đó tạo ra những câu chuyện có liên quan đến những điểm tiếp cận này nhất. 

Vào năm 2015, mức độ quan tâm của nhãn hiệu Coke đã tăng lên đáng kể (vì 

đảm bảo tính bảo mật nên con số thống kê không được nhắc tới ở đây). TVC 

của chúng tôi vẫn duy trì được sự thành công như những năm trước đó, nên có 

thể nói sự tăng lên này cũng nhờ một phần ở những câu chuyện truyền cảm mà 

khán giả đã chia sẻ rộng rãi trên mạng trực tuyến. Chúng tôi khuyến khích các 

bạn trẻ Việt đừng nên bị gò bó trong suy nghĩ về những món quà mắc tiền mà 

đánh mất cơ hội dành thời gian trở về ngôi nhà thân yêu của mình. Với hình thức 

kĩ thuật số dài hơn cho phép chúng tôi tạo ra được nhiều chi tiết hơn với những 

câu chuyện mang tính truyền cảm sâu sắc, điều này đã giúp chúng tôi gây được 

tiếng vang lớn trong cộng đồng khán giả. Nó đã trở thành đoạn quảng cáo được 

xem và được chia sẻ nhiều nhất trong vòng 24 tiếng đồng hồ (thậm chí còn đánh 

